var feet = 1000;
function brickCalculator(floor) {
    if (floor <= 10) {
        console.log(floor * (feet * 15));
    } 
    else if (floor > 10 && floor <= 20) {
        console.log(floor * (feet * 12));
    } 
    else {
        console.log(floor * (feet * 10));
    }

}

brickCalculator(17);
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 

